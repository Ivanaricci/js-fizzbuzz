
// numeri (da 1 a 100)
// multipli di 3 (Fizz)
// multipli di 5 (Buzz)
// multipli sia di 3 che di 5 (FizzBuzz)

// Se sono multipli di 3 stampare Fizz
// Se sono multipli di 5 stampare Buzz
// se sono multipli sia di 3 che di 5 stampare FizzBuzz







for(let num =1; num <= 100; num++){
    

    if(num % 3 === 0){
        console.log('Fizz');
}
    else if(num % 5 === 0){
        console.log('Buzz')
    }

}

